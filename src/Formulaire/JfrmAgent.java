    private void BTAJOUTERActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTAJOUTERActionPerformed
        // TODO add your handling code here:
        if (texteFldMat.getText().equals("") || textFldNom.getText().equals("")
                || texteFldPstnom.getText().equals("") || texteFldPrnom.getText().equals("")
                || comboSexe.getSelectedItem().equals("")
                || comboGrade.getSelectedItem().equals("")
                || comboBureau.getSelectedItem().equals("")
                || comboTerritoire.getSelectedItem().equals("")) {
            JOptionPane.showMessageDialog(null, "Veuillez remplir tous les champs", "ATTENTION!!", JOptionPane.ERROR_MESSAGE);
        } else {
            ag.setId_AGENT(texteFldMat.getText());
            ag.setNom(textFldNom.getText());
            ag.setPostnom(texteFldPstnom.getText());
            ag.setPrenom(texteFldPrnom.getText());
            ag.setSexe(comboSexe.getSelectedItem().toString());

            String idB, idG, idT;
            idG = gr.IDG(comboGrade.getSelectedItem().toString());
            ag.setId_GRADE(idG);
            idB = br.IDB(comboBureau.getSelectedItem().toString());
            ag.setId_BUREAU(idB);
            idT = tr.IDT(comboTerritoire.getSelectedItem().toString());
            ag.setId_TERRITOIRE(idT);
            ag.Ajouter();
            ag.Remplir(jTableAgent, "select AGENT.Id_AGENT,AGENT.Nom,AGENT.Postnom,AGENT.Prenom,AGENT.Sexe,\n"
                    + "GRADE.Libelle,BUREAU.Libelle,TERRITOIRE.Libelle\n"
                    + "from AGENT,GRADE,BUREAU,TERRITOIRE,MINISTERE,SECRETARIATGENERAL,DIRECTION,DIVISION,PROVINCE\n"
                    + ",DISTRICT\n"
                    + "where (AGENT.Id_GRADE=GRADE.Id_GRADE and (MINISTERE.Id_MINISTERE=SECRETARIATGENERAL.Id_MINISTERE\n"
                    + "and SECRETARIATGENERAL.Id_SECRETARIATGENERAL=DIRECTION.Id_SECRETARIATGENERAL and \n"
                    + "DIRECTION.Id_DIRECTION=DIVISION.Id_DIRECTION and DIVISION.Id_DIVISION=BUREAU.Id_DIVISION)and \n"
                    + "BUREAU.Id_BUREAU=AGENT.Id_BUREAU)and (PROVINCE.Id_PROVINCE=DISTRICT.Id_PROVINCE and\n"
                    + "DISTRICT.Id_DISTRICT=TERRITOIRE.Id_DISTRICT)and TERRITOIRE.Id_TERRITOIRE=AGENT.Id_TERRITOIRE");
            Nettoyer();

        }
    }//GEN-LAST:event_BTAJOUTERActionPerformed

